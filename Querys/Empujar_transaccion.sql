SELECT DISTINCT 
    b.SPRIDEN_ID
    --, a.TBRACCD_PIDM
    , a.TBRACCD_TRAN_NUMBER
    , a.TBRACCD_SURROGATE_ID
FROM TBRACCD a
    INNER JOIN SPRIDEN b ON
        a.TBRACCD_PIDM = b.SPRIDEN_PIDM
        AND b.SPRIDEN_CHANGE_IND IS NULL
WHERE b.SPRIDEN_ID = 'N00044530' --ID
AND a.TBRACCD_TRAN_NUMBER IN (11,12); --NRO TRANSACCION