SELECT DISTINCT
    a.ACADEMIC_PERIOD AS PERIODO
    , a.INSTRUCTION_METHOD AS MET_EDU, a.INSTRUCTION_METHOD_DESC
    , a.SUBJECT || '.' || a.COURSE_NUMBER || '.' || a.ACADEMIC_PERIOD || '.' || a.COURSE_REFERENCE_NUMBER || '.' || a.INSTRUCTION_METHOD AS SECCION
    , a.ID AS COD_BANNER, a.PERSON_UID AS PIDMUSUARIO
    , b.ENROLLMENT_STATUS, b.ENROLLMENT_STATUS_DESC
    , a.REGISTRATION_STATUS, a.REGISTRATION_STATUS_DESC
    , a.REGISTRATION_STATUS_DATE AS FECHA_REGISTRO
FROM STUDENT_COURSE a, ACADEMIC_STUDY b
WHERE a.PERSON_UID = b.PERSON_UID AND a.ACADEMIC_PERIOD = b.ACADEMIC_PERIOD
    AND a.ACADEMIC_PERIOD IN ('218434','218533','218735','218736','218342') AND a.REGISTRATION_STATUS IS NOT NULL
    /*AND a.ACADEMIC_PERIOD IN ('218434','218533','218735','218736','218342','218999','218829','218996','218929','218998','218931'
    ,'218932','218934','218933','218935','218826','218936','217813','218995','218837','218816','218938','218928','218994','218997'
    ,'218840','218941','218943','218843','218912','218917','218916','218915','218988','218920','218914','218986','218921','218923'
    ,'218985','217835','218925','218927','218937','218939','218942','217836','217818','217899','217424','218992')*/
ORDER BY a.ACADEMIC_PERIOD,a.PERSON_UID;
